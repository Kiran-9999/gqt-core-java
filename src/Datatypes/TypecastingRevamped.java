package Datatypes;

public class TypecastingRevamped {

	public static void main(String[] args) {
		long l=130l;
		System.out.println("Long Data="+l);
		int i=(int)l;
		System.out.println("Int Data:"+i);
		short s=(short)i;
		System.out.println("Short Data:"+s);
		byte b=(byte)s;
		System.out.println("Byte Data:"+b);

	}

}
